class Calculator {
  // Constructor function to create a Calculator instance
  constructor() {}

  add(a, b) {
    return a + b;
  }

  subtract(a, b) {
    return a - b;
  }

  multiply(a, b) {
    return a * b;
  }
}
  // Export the Calculator constructor so it can be used in other parts of your application
  module.exports= Calculator;
  